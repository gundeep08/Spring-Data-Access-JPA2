CREATE TABLE `BOOK_PUBLISHING`.`category` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `Name` VARCHAR(100) NOT NULL,
  PRIMARY KEY (`ID`));
  
  CREATE TABLE `BOOK_PUBLISHING`.`book` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `CATEGORY_ID` INT NOT NULL,
  `ISBN` VARCHAR(500) NOT NULL,
  `TITLE` VARCHAR(500) NOT NULL,
  `PRICE` DECIMAL(4,2) NOT NULL,
  PRIMARY KEY (`ID`),
  INDEX `CATEGORY_ID_idx` (`CATEGORY_ID` ASC) VISIBLE,
  CONSTRAINT `CATEGORY_ID`
    FOREIGN KEY (`CATEGORY_ID`)
    REFERENCES `BOOK_PUBLISHING`.`category` (`ID`)
    ON DELETE CASCADE
    ON UPDATE CASCADE);

    
  CREATE TABLE `BOOK_PUBLISHING`.`author` (
  `ID` INT NOT NULL AUTO_INCREMENT,
  `FIRST_NAME` VARCHAR(60) NOT NULL,
  `LAST_NAME` VARCHAR(60) NOT NULL,
  `DESCRIPTION` VARCHAR(1200) NULL,
  PRIMARY KEY (`ID`));
  
  CREATE TABLE `BOOK_PUBLISHING`.`author_book` (
  `AUTHOR_ID` INT NOT NULL,
  `BOOK_ID` INT NOT NULL,
  PRIMARY KEY (`AUTHOR_ID`, `BOOK_ID`),
  INDEX `BOOK_ID_idx` (`BOOK_ID` ASC) VISIBLE,
  CONSTRAINT `AUTHOR_ID`
    FOREIGN KEY (`AUTHOR_ID`)
    REFERENCES `BOOK_PUBLISHING`.`author` (`ID`)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
  CONSTRAINT `BOOK_ID`
    FOREIGN KEY (`BOOK_ID`)
    REFERENCES `BOOK_PUBLISHING`.`book` (`ID`)
    ON DELETE CASCADE
    ON UPDATE CASCADE);